"""""
Datos de Entrada

N Enteros Positivos = n = int 
K Enteros Positivos = k = int 

Datos de Salida
Siempre que k sea menor a n
Cuando N = K

"""""
n=int(input("Escriba el primer digito "))
k=int(input("Escriba el primer digito "))

while True:
    n=0
    if(k<n):
        n=n-1
        print(n)
    elif(n==k):
        print(k)
    break

